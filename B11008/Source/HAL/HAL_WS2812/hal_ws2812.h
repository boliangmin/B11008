#ifndef _HAL_WS2812_H_
#define _HAL_WS2812_H_



/************************************************************************************************************************************************
* include
************************************************************************************************************************************************/
#include <stdio.h>



/************************************************************************************************************************************************
* define
************************************************************************************************************************************************/
/* WS2812灯带通道数 */
#define HAL_WS2812_CHANNELS        4

/* WS2812灯带灯珠数 */
#define HAL_WS2812_CLK_RGB_NUM     31
#define HAL_WS2812_HT_RGB_NUM      31
#define HAL_WS2812_DAY_RGB_NUM     14
#define HAL_WS2812_FUN_RGB_NUM     4

/* clk,ht各位起始地址 */
#define HAL_WS2812_RIGHT2_ADDR     0
#define HAL_WS2812_RIGHT1_ADDR     7
#define HAL_WS2812_MIDDLE_ADDR     14
#define HAL_WS2812_LEFT2_ADDR      17
#define HAL_WS2812_LEFT1_ADDR      24

/* WS2812灯带通道名称 */
typedef enum WS2812_CHANNEL_TAG
{
    WS2812_CHANNEL_CLK = 0x00,
    WS2812_CHANNEL_HT,
    WS2812_CHANNEL_DAY,
    WS2812_CHANNEL_FUN,
    WS2812_CHANNEL_MAX
}ws2812_channel_t;

/* WS2812灯珠颜色 */
typedef struct WS2812_COLOR_TAG
{
    uint8_t red;
    uint8_t green;
    uint8_t blue;
}ws2812_color_t;

/* 工作日定义 */
typedef enum WEEK_DAY_TAG
{
    MON_DAY,
    TUE_DAY,
    WED_DAY,
    THU_DAY,
    FRI_DAY,
    SAT_DAY,
    SUN_DAY
}week_day_t;

/* 中央灯带显示类型 */
typedef enum SHOW_TYPE_TAG
{
    TYPE_DATE,
    TYPE_TIME,
    TYPE_MAX
}show_type_t;




/************************************************************************************************************************************************
* 函数名称: void HAL_WS2812_Init(void)
* 函数说明: 设备初始化
* 输入参数: 无
* 输出参数: 无
* 返回数值: 无
************************************************************************************************************************************************/
void HAL_WS2812_Init(void);

/************************************************************************************************************************************************
* 函数名称: void HAL_WS2812_SetShowType(show_type_t show_type)
* 函数说明: 设置中央区域显示时间or日期
* 输入参数: 无
* 输出参数: 无
* 返回数值: 无
************************************************************************************************************************************************/
void HAL_WS2812_SetShowType(show_type_t show_type);

/************************************************************************************************************************************************
* 函数名称: void HAL_WS2812_SetShowNum(uint8_t addr, uint8_t showNum)
* 函数说明: 数字显示
* 输入参数: 无
* 输出参数: 无
* 返回数值: 无
************************************************************************************************************************************************/
void HAL_WS2812_SetShowNum(uint8_t addr, uint8_t showNum);

/************************************************************************************************************************************************
* 函数名称: void HAL_WS2812_ClkFresh(ws2812_color_t color)
* 函数说明: 中央CLK区域显示
* 输入参数: 无
* 输出参数: 无
* 返回数值: 无
************************************************************************************************************************************************/
void HAL_WS2812_ClkFresh(ws2812_color_t color);

/************************************************************************************************************************************************
* 函数名称: void HAL_WS2812_ShowDay(uint8_t week_channel, uint8_t day_channel, week_day_t day, ws2812_color_t color)
* 函数说明: 工作日显示
* 输入参数: 无
* 输出参数: 无
* 返回数值: 无
************************************************************************************************************************************************/
void HAL_WS2812_ShowDay(uint8_t week_channel, uint8_t day_channel, week_day_t day, ws2812_color_t color);

/************************************************************************************************************************************************
* 函数名称: void HAL_WS2812_ShowFun(uint8_t channel, uint8_t showNum)
* 函数说明: 功能显示
* 输入参数: 无
* 输出参数: 无
* 返回数值: 无
************************************************************************************************************************************************/
void HAL_WS2812_ShowFun(uint8_t channel, uint8_t showNum);

/************************************************************************************************************************************************
* 函数名称: void HAL_WS2812_Close(uint8_t channel)
* 函数说明: 通道关闭
* 输入参数: 无
* 输出参数: 无
* 返回数值: 无
************************************************************************************************************************************************/
void HAL_WS2812_Close(uint8_t channel);



#endif



